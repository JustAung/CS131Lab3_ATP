
public class Square {
private double side;

public Square() {
	
}//end of default
public Square(double s) {
	super();
	this.side = side;
}//end of preferred

public double getArea() {
	return side;
}//get area method
/**
 * @param side
 */
@Override
public String toString() {
	return "Square [side=" + side + "]";
}

}
